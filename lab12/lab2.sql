-- 创建数据库
DROP DATABASE IF EXISTS microblogging;
CREATE DATABASE microblogging DEFAULT CHARSET utf8mb4;
USE microblogging;

-- 创建用户表
DROP TABLE IF EXISTS USERS;
CREATE TABLE USERS (
	UID INT PRIMARY KEY,
	NAME CHAR(40),
	SEX CHAR(4),
	BYEAR INT,
	CITY CHAR(40)
);

-- 创建分类标签表
DROP TABLE IF EXISTS LABEL;
CREATE TABLE LABEL (
	LID INT PRIMARY KEY,
	LNAME CHAR(40)
);

-- 创建博文表
DROP TABLE IF EXISTS MBLOG;
CREATE TABLE MBLOG (
	BID INT PRIMARY KEY,
	TITLE CHAR(40),
	UID INT,
	PYEAR INT,
	PMONTH INT,
	PDAY INT,
	CONT VARCHAR(4000)
);

-- 创建博文标注表
DROP TABLE IF EXISTS B_L;
CREATE TABLE B_L (
	BID INT,
	LID INT
);

-- 创建关注表
DROP TABLE IF EXISTS FOLLOW;
CREATE TABLE FOLLOW (
	UID INT,
	UIDFLED INT
);

-- 创建好友表
DROP TABLE IF EXISTS FRIENDS;
CREATE TABLE FRIENDS (
	UID INT,
	UIDFLED INT
);

-- 创建订阅表
DROP TABLE IF EXISTS SUB;
CREATE TABLE SUB (
	UID INT,
	LID INT
);

-- 创建点赞表
DROP TABLE IF EXISTS THUMB;
CREATE TABLE THUMB (
	UID INT,
	BID INT
);

-- 创建头条表
DROP TABLE IF EXISTS TOPDAY;
CREATE TABLE TOPDAY (
	TYEAR INT,
	TMONTH INT,
	TDAY INT,
	BID INT,
	TNO INT
);

